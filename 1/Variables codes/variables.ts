// Integer Data type
var n1:number = 10;
console.log(n1);

//String Data type
var s1:string = "You are the creator of your destiny";
var s2:string = `All the power is with in you , you can do anything and everything`;
console.log(s1);
console.log(s2);

// Boolean data type
var b1:boolean = true;
var b2:boolean = false;

console.log(b1);
console.log(b2);


// any data type
var a:any = {
    productId:1,
    ProductName: "Iphone 15 Pro Max ",
    ProductPrice: 2000
};

console.log(a);

// Homogeneous Array
var array1:Array<string> = ['Mango','Grapes','Apple']
console.log(array1);
console.log(array1[0]);
console.log(array1.length);


// Hetrogeneous Array

var array:Array<any> = [1,'Grapes','Apple']
console.log(array);




